const a = 7 // atribui o valor 7 dentro da constante a
let b = 3

b += a // atribuição aditiva, adiciona o valor de 'a' à variavel b
console.log(b) // é equivalente a b = b + a

b -= 4 //b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2 
console.log(b)

b %= 2 // b = b % 2 atribuição modular, resto da divisao de b por 2